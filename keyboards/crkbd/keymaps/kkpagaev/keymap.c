bool afk = true;

static void render_animation(void) {
    if (last_input_activity_elapsed() > OLED_SCREENSAVER_TIMEOUT) {
        afk = true;
    } else {
        afk = false;
    }
    if (afk ) {
        if (timer_elapsed(timer) > 500) {
            timer = timer_read();
            current_frame = (current_frame + 1) % 2;

            oled_write_raw_P(animation_imgs[current_frame], frame_size);
        }
        return;
    }

    if (current_frame >= 14) {
        if (timer_elapsed(timer) > 200) {
            timer = timer_read();
            if (current_frame == 15) {
                current_frame = 14;
            } else {
                current_frame = 15;
            }
            oled_write_raw_P(animation_imgs[current_frame], frame_size);
        }
        return;
    }

    if (timer_elapsed(timer) > 60) {
        timer = timer_read();
        current_frame = (current_frame + 1) % frame_count;
        oled_write_raw_P(animation_imgs[current_frame], frame_size);
    }
}

bool oled_task_user(void) {
    oled_set_brightness(0);
    render_animation();
    // arasaka_draw();
    return false;
}

bool should_process_keypress(void) { return true; }

bool is_alt_tab_active = false;
bool is_win_active = false;
bool is_ctrl_tab_active = false;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case ALTTAB: // ALT+TAB
            if (record->event.pressed) {
                if (!is_alt_tab_active) {
                    is_alt_tab_active = true;
                    register_code(KC_LALT);
                }
                register_code(KC_TAB);
            } else {
                unregister_code(KC_TAB);
            }
            return true;
            break;
        case CTAB: // CTRL+TAB
            if (record->event.pressed) {
                if (!is_ctrl_tab_active) {
                    is_ctrl_tab_active = true;
                    register_code(KC_LCTL);
                }
                register_code(KC_TAB);
            } else {
                unregister_code(KC_TAB);
            }
            return true;
            break;
        case STAB: // CTRL+TAB
            if (record->event.pressed) {
                if (!is_win_active) {
                    is_win_active = true;
                    register_code(KC_LGUI);
                }
                register_code(KC_TAB);
            } else {
                unregister_code(KC_TAB);
            }
            return false;
            break;
        case SGRV: // CTRL+TAB
            if (record->event.pressed) {
                if (!is_win_active) {
                    is_win_active = true;
                    register_code(KC_LGUI);
                }
                register_code(KC_GRV);
            } else {
                unregister_code(KC_GRV);
            }
            return false;
            break;
        case SALTTAB: // ALT+SHIFT+TAB
            if (record->event.pressed) {
                if (!is_alt_tab_active) {
                    is_alt_tab_active = true;
                    register_code(KC_LALT);
                }
                register_code16(S(KC_TAB));
            } else {
                unregister_code16(S(KC_TAB));
            }
            return false;
            break;
    }
    return true;
}

void matrix_scan_user(void) {
    if (is_win_active) {
        if (IS_LAYER_OFF(1)){
            unregister_code(KC_LGUI);
            is_win_active = false;
        }
    }

    if (is_alt_tab_active) {
        if (IS_LAYER_OFF(1)){
            unregister_code(KC_LALT);
            is_alt_tab_active = false;
        }
    }

    if (is_ctrl_tab_active) {
        if (IS_LAYER_OFF(1)){
            unregister_code(KC_LCTL);
            is_ctrl_tab_active = false;
        }
    }
}
